// Point the web app at this server with zero manual setup. .env.local is
// gitignored, so a fresh clone won't have it — we write it here on boot.
function ensureWebEnv() {
  const envPath = path.resolve(__dirname, '../web/.env.local')
  if (fs.existsSync(envPath)) return
  const body = [
    '# Auto-generated by apps/local-api — points the web app at the local DuckDB server.',
    '# Delete this file (or change the values) to use a real Supabase project instead.',
    `VITE_SUPABASE_URL=http://localhost:${PORT}`,
    'VITE_SUPABASE_ANON_KEY=local-dev-anon-key',
    '',
  ].join('\n')
  fs.writeFileSync(envPath, body)
  console.log('📝 Wrote apps/web/.env.local (pointing at the local DuckDB server)')
}

