function broadcastIntakeEvent(payload) {
  const data = JSON.stringify(payload)
  for (const res of sseClients.values()) {
    try { res.write(`data: ${data}\n\n`) } catch {}
  }
}

async function initDuckDB() {
  // DUCKDB_PATH env var → file-backed DB (survives container restart in dev Docker).
  // Unset (default) → in-memory, same behaviour as before.
  const dbPath = process.env.DUCKDB_PATH || ':memory:'
  const instance = await DuckDBInstance.create(dbPath)
  conn = await instance.connect()
}

function esc(val) {
  if (val === null || val === undefined) return 'NULL'
  if (typeof val === 'number') return isNaN(val) ? 'NULL' : String(val)
  if (typeof val === 'boolean') return val ? 'TRUE' : 'FALSE'
  return `'${String(val).replace(/'/g, "''")}'`
}

function interpolate(sql, params) {
  if (!params || params.length === 0) return sql
  let i = 0
  return sql.replace(/\?/g, () => esc(params[i++]))
}

async function run(sql, params = []) {
  await conn.run(interpolate(sql, params))
}

async function all(sql, params = []) {
  const reader = await conn.runAndReadAll(interpolate(sql, params))
  return reader.getRowObjectsJson()
}

function parseJson(val) {
  if (typeof val !== 'string') return val
  const t = val.trim()
  if ((t.startsWith('{') || t.startsWith('[')) ) {
    try { return JSON.parse(t) } catch {}
  }
  return val
}

function transformRow(row) {
  const out = {}
  for (const [k, v] of Object.entries(row)) {
    out[k] = parseJson(v)
  }
  return out
}

// Mirror of parseJson, for the write path: object/array values (e.g. a jsonb
// column sent as a real JSON body value, not a pre-stringified string) must
// be stringified before binding — otherwise the DuckDB driver coerces them
// with a plain toString(), storing the literal text "[object Object]".
function serializeJson(val) {
  if (val !== null && typeof val === 'object') return JSON.stringify(val)
  return val
}

function stringifyRow(row) {
  const out = {}
  for (const [k, v] of Object.entries(row)) {
    out[k] = serializeJson(v)
  }
  return out
}

