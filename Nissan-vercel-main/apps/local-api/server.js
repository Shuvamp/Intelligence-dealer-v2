// Sign in
app.post('/auth/v1/token', async (req, res) => {
  if (req.query.grant_type !== 'password') return res.status(400).json({ error: 'unsupported grant_type' })
  const { email, password } = req.body || {}
  if (!email || !password) return res.status(400).json({ error: 'email and password required' })

  const hash = crypto.createHash('sha256').update(password).digest('hex')
  const rows = await all(`SELECT * FROM users WHERE email=? AND password_hash=?`, [email, hash])
  const user = rows[0]
  if (!user) return res.status(400).json({ error: 'Invalid login credentials', error_code: 'invalid_credentials' })

  const accessToken = jwt.sign(
    { sub: user.id, email: user.email, role: 'authenticated', tenant_id: user.tenant_id, iss: 'supabase', aud: 'authenticated' },
    JWT_SECRET, { expiresIn: '7d' }
  )
  res.json({
    access_token: accessToken,
    token_type: 'bearer',
    expires_in: 604800,
    expires_at: Math.floor(Date.now() / 1000) + 604800,
    refresh_token: uuidv4(),
    user: {
      id: user.id, email: user.email, role: 'authenticated',
      app_metadata: { tenant_id: user.tenant_id },
      user_metadata: { full_name: user.full_name },
      created_at: user.created_at,
    },
  })
})

