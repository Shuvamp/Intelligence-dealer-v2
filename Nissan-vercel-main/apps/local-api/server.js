// ─── Lead Intake Logic ────────────────────────────────────────────────────────

// Fire-and-forget: automatically run the FastAPI Follow-up Agent (apps/api) for a
// freshly created lead — the same thing the Leads "Generate follow-up" button does,
// but triggered automatically on lead arrival. Non-blocking so intake stays fast;
// if the agent service is down it just logs a warning. Toggle with AUTO_FOLLOWUP=0.
const AGENT_API_URL = process.env.AGENT_API_URL || 'http://localhost:8000'
const AUTO_FOLLOWUP = process.env.AUTO_FOLLOWUP !== '0'

function triggerFollowupAgent(leadId) {
  if (!AUTO_FOLLOWUP || !leadId) return
  fetch(`${AGENT_API_URL}/followup/${leadId}`, { method: 'POST' })
    .then((r) => {
      if (r.ok) console.log(`🤝 auto follow-up done for lead ${leadId}`)
      else console.warn(`[auto-followup] agent returned ${r.status} for ${leadId}`)
    })
    .catch((e) => console.warn(`[auto-followup] agent unreachable (${AGENT_API_URL}): ${e.message}`))
}

// Dynamic Re-Scoring (Phase 6) — fire-and-forget, same pattern as triggerWorkflowAgent.
// Toggle with AUTO_RESCORE=0.
const AUTO_RESCORE = process.env.AUTO_RESCORE !== '0'

function triggerRescore(leadId, trigger = 'stage_change') {
  if (!AUTO_RESCORE || !leadId) return
  fetch(`${AGENT_API_URL}/rescore/${leadId}`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ trigger }),
  })
    .then((r) => {
      if (r.ok) console.log(`📊 rescore done for lead ${leadId} (trigger=${trigger})`)
      else console.warn(`[rescore] agent returned ${r.status} for ${leadId}`)
    })
    .catch((e) => console.warn(`[rescore] agent unreachable: ${e.message}`))
}

// Same fire-and-forget shape as triggerFollowupAgent, for the Workflow Agent
// (Phase 3). The decision itself is made + persisted inside FastAPI; this
// shim only needs the response back so it can broadcast a `workflow_action`
// SSE event on ITS OWN sseClients — the one the browser's EventSource
// actually connects to in local dev (it points at VITE_SUPABASE_URL, i.e.
// this shim, not :8000 — same reasoning as the stage_change event in
// server.js's /events/stage-change). Toggle with AUTO_WORKFLOW=0.
const AUTO_WORKFLOW = process.env.AUTO_WORKFLOW !== '0'

function triggerWorkflowAgent(leadId, { customerName, vehicleInterest } = {}) {
  if (!AUTO_WORKFLOW || !leadId) return
  fetch(`${AGENT_API_URL}/workflow/${leadId}`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ trigger_source: 'intake' }),
  })
    .then(async (r) => {
      if (!r.ok) {
        console.warn(`[auto-workflow] agent returned ${r.status} for ${leadId}`)
        return
      }
      const decision = await r.json()
      console.log(`🧭 auto workflow done for lead ${leadId}: ${(decision.actions || []).join(', ')}`)
      broadcastIntakeEvent({
        type: 'workflow_action',
        lead_id: leadId,
        classification: decision.classification,
        actions: decision.actions,
        escalated: decision.escalated,
        customer_name: customerName ?? null,
        vehicle_interest: vehicleInterest ?? null,
      })
    })
    .catch((e) => console.warn(`[auto-workflow] agent unreachable (${AGENT_API_URL}): ${e.message}`))
}

async function processIntakeLead(rawLead, source) {
  // Run the full pipeline: validate → normalize → score → assign.
  // validate.node.js calls the FastAPI lead_validator which persists customer+lead
  // and writes _lead_id / _customer_id back onto rawLead.
  const { normalized, scoring, assignment } = await intakeAgent.process(rawLead, source)
  const tenantId = ABC_TENANT_ID
  const now = new Date().toISOString()

  // Pipeline outputs (with safe fallbacks while teammate agents are stubs).
  const score      = scoring?.score ?? 'warm'
  const scoreValue = scoring?.score_value ?? 50
  const assignedTo = assignment?.assigned_to ?? null
  // Agent attribution: how was this lead scored? The Python agent reports
  // "groq_holistic" / "deterministic" in scoring.detail; the JS fallback has no
  // detail, so we tag it "static_js". This lets the UI show which agent ran.
  const scoredBy = scoring?.detail?.scored_by ?? (scoring ? 'static_js' : null)
  const scoreReasons = JSON.stringify(scoring?.reasons ?? [])
  // Optional human-facing notice from the scoring agent (e.g. fallback warning); null = normal.
  const scoreNotice = scoring?.score_notice ?? scoring?.detail?.score_notice ?? null

  // Intake preferences captured from the source form / demo.
  const testDrive    = normalized.test_drive_required ? true : false
  const budget       = normalized.budget ?? null
  const buyTimeline  = normalized.buy_timeline_days ?? null
  const callbackDays = normalized.callback_days ?? null
  const contactMedium = normalized.contact_medium ?? null
  // Enquiry-form signal fields (persisted so the lead detail can show them back).
  const financing = normalized.financing ?? null
  const nissanRelationship = normalized.nissan_relationship ?? null
  const brandConsideration = normalized.brand_consideration ?? null
  const comparingBrands = normalized.comparing_brands ?? null
  const purchaseReason = normalized.purchase_reason ?? null

  let leadId     = rawLead._lead_id     ?? null
  let customerId = rawLead._customer_id ?? null

  if (leadId && rawLead._is_duplicate) {
    // Validation Agent (Phase 1): a repeat enquiry only gets enquiry_count
    // incremented + a lead_interactions record — both already done by
    // lead_validator's dedup_and_persist. This submission's freshly computed
    // score/assignment are NOT written over the existing lead's row (still
    // surfaced in the API response/broadcast below for visibility).
  } else if (leadId) {
    // FastAPI validator already inserted customer + lead — patch with pipeline outputs.
    await run(
      `UPDATE leads SET score=?, score_value=?, assigned_to=?,
        vehicle_interest=?, budget=?, test_drive_required=?,
        purchase_timeline_days=?, callback_within_days=?, contact_medium=?,
        financing=?, nissan_relationship=?, brand_consideration=?, comparing_brands=?, purchase_reason=?,
        scored_by=?, score_reasons=?, score_notice=?,
        updated_at=?, last_activity_at=?
       WHERE id=?`,
      [score, scoreValue, assignedTo,
       normalized.vehicle, budget, testDrive,
       buyTimeline, callbackDays, contactMedium,
       financing, nissanRelationship, brandConsideration, comparingBrands, purchaseReason,
       scoredBy, scoreReasons, scoreNotice,
       now, now,
       leadId],
    )
  } else {
    // Fallback: FastAPI was unreachable — insert directly.
    customerId = uuidv4()
    leadId     = uuidv4()
    await run(
      `INSERT INTO customers (id,tenant_id,location_id,full_name,phone,email,preferred_vehicle,source_channel,created_at) VALUES (?,?,?,?,?,?,?,?,?)`,
      [customerId, tenantId, LOC_VEL_ID, normalized.name, normalized.phone, normalized.email || null, normalized.vehicle, source, now]
    )
    await run(
      `INSERT INTO leads (id,tenant_id,location_id,customer_id,source,stage,score,score_value,assigned_to,vehicle_interest,budget,notes,test_drive_required,purchase_timeline_days,callback_within_days,contact_medium,financing,nissan_relationship,brand_consideration,comparing_brands,purchase_reason,scored_by,score_reasons,score_notice,created_at,updated_at,last_activity_at) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)`,
      [leadId, tenantId, LOC_VEL_ID, customerId, source, 'new', score, scoreValue, assignedTo, normalized.vehicle, budget, null, testDrive, buyTimeline, callbackDays, contactMedium, financing, nissanRelationship, brandConsideration, comparingBrands, purchaseReason, scoredBy, scoreReasons, scoreNotice, now, now, now]
    )
  }

  // ── Agent attribution timeline ──────────────────────────────────────────────
  // Record what each pipeline agent did, in order, so the lead detail page can
  // show exactly which agent produced which value (validate→normalize→score→assign).
  const scoredByLabel = scoredBy === 'claude_holistic' ? 'Claude' : scoredBy === 'groq_holistic' ? 'Groq LLM' : scoredBy === 'deterministic' ? 'agent heuristic' : 'static fallback'
  const agentEvents = [
    { agent: 'Validate Agent · Amirtha',  stage: 1, summary: `Validated lead — name & phone present, accepted into pipeline.` },
    { agent: 'Normalize Agent · Partha',  stage: 2, summary: `Normalized fields — ${normalized.vehicle ?? 'no vehicle'}, ${budget ? '₹' + Math.round(budget / 100000) + 'L' : 'no budget'}, ${testDrive ? 'test drive' : 'no test drive'}.` },
    { agent: 'Scoring Agent · Csriram',   stage: 3, summary: `Scored ${String(score).toUpperCase()} (${scoreValue}/100) via ${scoredByLabel}.` },
    { agent: 'Assignment Agent · Keerthana', stage: 4, summary: assignment?.assignee_name ? `Assigned to ${assignment.assignee_name} (least-loaded executive).` : `No executive available to assign.` },
  ]
  const nowMs = Date.parse(now)
  for (const ev of agentEvents) {
    // Stagger by stage so the pipeline reads validate→normalize→score→assign in order.
    const ts = new Date(nowMs + ev.stage * 1000).toISOString()
    await run(
      `INSERT INTO lead_events (id,tenant_id,lead_id,type,summary,metadata,created_by,created_at) VALUES (?,?,?,?,?,?,?,?)`,
      [uuidv4(), tenantId, leadId, 'agent', ev.summary, JSON.stringify({ agent: ev.agent, stage: ev.stage }), null, ts]
    )
  }

  const assignNote = assignment?.assignee_name ? ` · assigned to ${assignment.assignee_name}` : ''
  await run(
    `INSERT INTO notifications (id,tenant_id,title,message,status,created_at) VALUES (?,?,?,?,?,?)`,
    [
      uuidv4(),
      tenantId,
      `New ${source} lead (${score})`,
      `${normalized.name} enquired about ${normalized.vehicle ?? 'a vehicle'}${normalized.city ? ' from ' + normalized.city : ''}${assignNote}.`,
      'unread',
      now,
    ]
  )

  const lead = {
    id: leadId,
    customer_name: normalized.name,
    source,
    stage: 'new',
    score,
    score_value: scoreValue,
    assigned_to: assignedTo,
    assignee_name: assignment?.assignee_name ?? null,
    vehicle_interest: normalized.vehicle,
    city: normalized.city,
    budget,
    test_drive_required: testDrive,
    purchase_timeline_days: buyTimeline,
    callback_within_days: callbackDays,
    contact_medium: contactMedium,
    financing,
    nissan_relationship: nissanRelationship,
    brand_consideration: brandConsideration,
    comparing_brands: comparingBrands,
    purchase_reason: purchaseReason,
    scored_by: scoredBy,
    score_notice: scoreNotice,
    created_at: now,
  }
  broadcastIntakeEvent({ type: 'new_lead', lead, source })
  // Auto-run the follow-up agent for the new lead (non-blocking).
  triggerFollowupAgent(leadId)
  // Auto-run the Workflow Agent (Phase 3) — decides next action(s), persists
  // a workflow_actions/lead_tasks/lead_events record, escalates if hot.
  triggerWorkflowAgent(leadId, { customerName: normalized.name, vehicleInterest: normalized.vehicle })
  pushLog({
    type: 'success',
    source,
    lead_id: leadId,
    customer_name: normalized.name,
    phone: normalized.phone,
    vehicle: normalized.vehicle,
    score,
    is_duplicate: !!rawLead._is_duplicate,
  })
  return { success: true, lead, scoring, assignment }
}

