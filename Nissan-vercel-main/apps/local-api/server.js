// Parse `col=eq.value`, `col=not.is.null` etc from query string
function parseFilters(query, skipKeys = []) {
  const skip = new Set(['select', 'order', 'limit', 'offset', 'head', ...skipKeys])
  const filters = []
  const params = []

  for (const [key, val] of Object.entries(query)) {
    if (skip.has(key)) continue
    const str = String(val)

    // not.is.null
    if (str === 'not.is.null') { filters.push(`${key} IS NOT NULL`); continue }
    if (str === 'is.null')     { filters.push(`${key} IS NULL`);     continue }

    const m = str.match(/^(eq|neq|gt|lt|gte|lte|like|ilike)\.(.*)$/)
    if (!m) continue
    const [, op, v] = m
    const sqlOp = { eq: '=', neq: '!=', gt: '>', lt: '<', gte: '>=', lte: '<=', like: 'LIKE', ilike: 'ILIKE' }[op]
    filters.push(`${key} ${sqlOp} ?`)
    params.push(v)
  }
  return { filters, params }
}

function buildWhere(filters) {
  return filters.length ? `WHERE ${filters.join(' AND ')}` : ''
}

function buildOrder(orderParam) {
  if (!orderParam) return ''
  const parts = String(orderParam).split(',').map(o => {
    const [col, dir] = o.trim().split('.')
    return `${col} ${dir?.toUpperCase() === 'DESC' ? 'DESC' : 'ASC'}`
  })
  return `ORDER BY ${parts.join(', ')}`
}

// Detect join selects like: customer:customers!leads_customer_id_fkey(full_name)
const JOIN_RE = /(\w+):(\w+)![\w_]*\(([^)]+)\)/g

function parseSelect(selectRaw, table) {
  const joins = []
  let clean = selectRaw

  for (const m of selectRaw.matchAll(new RegExp(JOIN_RE.source, 'g'))) {
    const [full, alias, joinTable, cols] = m
    joins.push({ alias, joinTable, cols: cols.split(',').map(c => c.trim()) })
    clean = clean.replace(full, '')
  }
  // Also handle simpler: campaign:campaigns(name)
  const SIMPLE_JOIN_RE = /(\w+):(\w+)\(([^)]+)\)/g
  for (const m of clean.matchAll(new RegExp(SIMPLE_JOIN_RE.source, 'g'))) {
    const [full, alias, joinTable, cols] = m
    joins.push({ alias, joinTable, cols: cols.split(',').map(c => c.trim()) })
    clean = clean.replace(full, '')
  }

  // Clean up leftover commas
  clean = clean.split(',').map(c => c.trim()).filter(Boolean).join(', ') || '*'

  return { baseCols: clean, joins }
}

// Build the SQL for joins
function buildJoinSql(table, baseCols, joins, where, order, limit, offset) {
  const joinClauses = []
  const extraSelects = []
  const fkMap = {
    // leads → customers via customer_id
    customers: { fk: `${table}.customer_id`, pk: 'customers.id' },
    // leads → users via assigned_to
    users:     { fk: `${table}.assigned_to`,  pk: 'users.id' },
    // campaign_posts → campaigns via campaign_id
    campaigns: { fk: `${table}.campaign_id`,  pk: 'campaigns.id' },
  }
  for (const { alias, joinTable, cols } of joins) {
    const fk = fkMap[joinTable]
    if (!fk) continue
    const safeAlias = `j_${alias}`
    joinClauses.push(`LEFT JOIN ${joinTable} AS ${safeAlias} ON ${fk.fk} = ${safeAlias}.${fk.pk.split('.')[1]}`)
    for (const col of cols) {
      extraSelects.push(`${safeAlias}.${col} AS __${alias}__${col}`)
    }
  }

  const selectPart = [
    baseCols === '*' ? `${table}.*` : baseCols.split(',').map(c => `${table}.${c.trim()}`).join(', '),
    ...extraSelects,
  ].join(', ')

  // When joining, unqualified column refs in WHERE are ambiguous (e.g. "id" exists in
  // leads, customers, and users). Prefix every bare column name in the WHERE clause
  // with the main table so DuckDB can resolve them without error.
  let qualifiedWhere = where
  if (joinClauses.length > 0 && where) {
    // Replace patterns like "WHERE colname OP" and "AND colname OP"
    // where colname has no dot (unqualified).
    qualifiedWhere = where.replace(
      /(WHERE|AND|OR)\s+([a-zA-Z_][a-zA-Z0-9_]*)\s*(=|!=|<>|>=|<=|>|<|IS\s+NOT\s+NULL|IS\s+NULL|LIKE|ILIKE)/gi,
      (m, kw, col, op) => `${kw} ${table}.${col} ${op}`
    )
  }

  const sql = `SELECT ${selectPart} FROM ${table} ${joinClauses.join(' ')} ${qualifiedWhere} ${order} ${limit} ${offset}`
  return { sql, joins }
}

// Reshape rows: pull __alias__col fields into nested objects
function reshapeJoinRows(rows, joins) {
  return rows.map(row => {
    const out = {}
    const nested = {}

    for (const { alias, cols } of joins) {
      nested[alias] = {}
      for (const col of cols) {
        const key = `__${alias}__${col}`
        nested[alias][col] = row[key] ?? null
        // Don't include in flat row
      }
    }

    for (const [k, v] of Object.entries(row)) {
      if (k.startsWith('__')) continue
      out[k] = parseJson(v)
    }

    for (const [alias, obj] of Object.entries(nested)) {
      // If all values are null, the join had no match
      const hasData = Object.values(obj).some(v => v !== null)
      out[alias] = hasData ? obj : null
    }

    return out
  })
}

app.all('/rest/v1/:table', async (req, res) => {
  const { table } = req.params
  const method = req.method
  const q = req.query
  const prefer = String(req.headers['prefer'] || '')
  const accept = String(req.headers['accept'] || '')
  const wantsCount = prefer.includes('count=exact')
  const headOnly = q.head === 'true'
  // .single() / .maybeSingle() send this Accept header and expect ONE object, not an array.
  const singleObject = accept.includes('application/vnd.pgrst.object+json')

  const selectRaw = String(q.select || '*')
  const { filters, params: filterParams } = parseFilters(q)
  const where = buildWhere(filters)
  const order = buildOrder(q.order)
  const limitClause  = q.limit  ? `LIMIT ${parseInt(q.limit)}`   : ''
  const offsetClause = q.offset ? `OFFSET ${parseInt(q.offset)}` : ''

  try {
    // ── GET / HEAD ──
    // supabase-js sends HTTP HEAD (not GET+head=true) for .select('*',{count:'exact',head:true})
    if (method === 'GET' || method === 'HEAD') {
      let count = 0
      if (wantsCount || headOnly || method === 'HEAD') {
        const rows = await all(`SELECT COUNT(*) AS cnt FROM ${table} ${where}`, filterParams)
        count = Number(rows[0]?.cnt ?? 0)
        res.setHeader('Content-Range', `0-${Math.max(count - 1, 0)}/${count}`)
        res.setHeader('Range-Unit', 'items')
        if (headOnly || method === 'HEAD') return res.status(200).end()
      }

      const { baseCols, joins } = parseSelect(selectRaw, table)

      let rows
      if (joins.length > 0) {
        const { sql } = buildJoinSql(table, baseCols, joins, where, order, limitClause, offsetClause)
        const raw = await all(sql, filterParams)
        rows = reshapeJoinRows(raw, joins)
      } else {
        const cols = baseCols === '*' ? '*' : baseCols
        rows = (await all(`SELECT ${cols} FROM ${table} ${where} ${order} ${limitClause} ${offsetClause}`, filterParams))
          .map(transformRow)
      }

      if (!wantsCount) {
        res.setHeader('Content-Range', `0-${Math.max(rows.length - 1, 0)}/${rows.length}`)
      }
      // .single()/.maybeSingle() expect a single object (or null), not an array.
      if (singleObject) return res.json(rows[0] ?? null)
      return res.json(rows)
    }

    // ── POST (insert) ──
    if (method === 'POST') {
      const body = Array.isArray(req.body) ? req.body : [req.body]
      const inserted = []
      for (const row of body) {
        if (!row.id) row.id = uuidv4()
        if (!row.created_at) row.created_at = new Date().toISOString()
        const cols = Object.keys(row).join(', ')
        const ph   = Object.keys(row).map(() => '?').join(', ')
        await run(`INSERT INTO ${table} (${cols}) VALUES (${ph})`, Object.values(stringifyRow(row)))
        inserted.push(row)
      }
      if (singleObject) return res.status(201).json(inserted[0] ?? null)
      return res.status(201).json(inserted)
    }

    // ── PATCH (update) ──
    if (method === 'PATCH') {
      if (!req.body || Object.keys(req.body).length === 0) return res.status(204).send()
      const sets = Object.keys(req.body).map(k => `${k} = ?`).join(', ')
      await run(`UPDATE ${table} SET ${sets} ${where}`, [...Object.values(stringifyRow(req.body)), ...filterParams])
      return res.status(204).send()
    }

    // ── DELETE ──
    if (method === 'DELETE') {
      await run(`DELETE FROM ${table} ${where}`, filterParams)
      return res.status(204).send()
    }

    res.status(405).json({ error: 'Method not allowed' })
  } catch (err) {
    console.error(`[${method} /rest/v1/${table}]`, err.message)
    res.status(500).json({ message: err.message, hint: err.message })
  }
})

// ─── Helper: Extract tenant from JWT ──────────────────────────────────────────
function getTenantFromAuth(req) {
  const auth = req.headers.authorization || ''
  if (!auth.startsWith('Bearer ')) return null
  try {
    const decoded = jwt.verify(auth.slice(7), JWT_SECRET)
    return decoded.tenant_id
  } catch {
    return null
  }
}

